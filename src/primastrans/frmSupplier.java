    private void load_table(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Supplier");
        model.addColumn("Kode DO");
        model.addColumn("DO");
        model.addColumn("Stock Pile");
        model.addColumn("Tempat Bongkar");
        model.addColumn("Kas");
        model.addColumn("Kas Primas");
        model.addColumn("Kord");
        model.addColumn("Tab");
        
        //menampilkan data database kedalam tabel
        try {
            String sql = "select * from tb_supplier ORDER BY LENGTH(id_supplier) asc, id_supplier asc";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8),res.getString(9)});
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    

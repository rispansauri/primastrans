    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        // TODO add your handling code here:
        try{
            String query = "select * from tb_supplier where id_supplier = '"+txtIDSupp.getText()+"'";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(query);
            if(res.next()){
                String sql ="UPDATE tb_supplier SET id_supplier = '"+txtIDSupp.getText()+"', kd_do = '"+txtKdDO.getText()+"', do = '"+txtDO.getText()+"',stockpile = '"+txtStockpile.getText()+"',tempat_bongkar = '"+txtTempatBongkar.getText()+"',kas = '"+txtKas.getText()+"',kas_primas = '"+txtKasPrimas.getText()+"',kord = '"+txtKord.getText()+"',tab = '"+txtTab.getText()+"' WHERE id_supplier = '"+txtIDSupp.getText()+"'";
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "data berhasil diedit");
            }else{
                String sql = "INSERT INTO tb_supplier VALUES ('"+txtIDSupp.getText()+"', '"+txtKdDO.getText()+"', '"+txtDO.getText()+"', '"+txtStockpile.getText()+"', '"+txtTempatBongkar.getText()+"', '"+txtKas.getText()+"', '"+txtKasPrimas.getText()+"', '"+txtKord.getText()+"', '"+txtTab.getText()+"')";
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Penyimpanan data berhasil");
            }
            load_table();
            textboxOff();
            kosong();
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanMouseClicked


    private void idSupp() {
        try {
            String sql = "select * from tb_supplier ORDER BY LENGTH(id_supplier) desc, id_supplier desc";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet rs=stm.executeQuery(sql);
            if (rs.next()) {
                String kd = rs.getString("id_supplier").substring(3);
                String AN = "" + (Integer.parseInt(kd) + 1);
                txtIDSupp.setText("SPL" + AN);
            } else {
                txtIDSupp.setText("SPL1");
            }

           }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
           }
    }
    
